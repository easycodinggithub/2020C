// 시험 문제 출제

#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <math.h>

typedef struct Point PP;

struct Point
{
	int x, y;
};

int main() {

	double a, b, c;

	PP p1;
	PP p2;

	scanf("%d %d", &p1.x, &p1.y);

	scanf("%d %d", &p2.x, &p2.y);

	a = (p1.x - p2.x);

	b = (p1.y - p2.y);

	c = sqrt(pow(a, 2) + pow(b, 2));

	//c = sqrt(a * a + b * b);

	printf("%.2f", c);

	return 0;
}// 시험 문제 출제

